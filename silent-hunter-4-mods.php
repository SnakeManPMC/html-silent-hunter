<h2>Real Fleet Boat (RFO)</h2>

<p><a href="http://www.subsim.com/radioroom/showthread.php?t=159709">subsim forums</a> by unknown authors
<br><br>
Unkown feature list as its missing from release topic (as usual). Seems to be ultra realistic hardcore mod.
</p>

<p>Water is colored candy blue, very artificial looking. Camera view Home and End keys do not adjust camera height. Captains position adjusted to middle of the bridge, also bridge crew positions look different than default (your binocular view is blocked by these guys). Inside sub stations camera panning mouse control feels like its been adjusted to smoother (not so twitchy as default). Sub stations etc icons in user interface are annoyingly transparent, very difficult to see what is what.
</p>

<p>Most horrifying feature is that ... it enables no map contacts update automatically (or rather disables any ship icons), your own sub icon is just a dot. Good luck playing without map contacts then unless you are one of those veteran purists who want to play like this :(
</p>

<h2>Trigger Maru Overhauled (TMO)</h2>
<p><a href="http://www.subsim.com/radioroom/showthread.php?t=132330">subsim forums</a> by Ducimus
<br><br>
According to manual: Ship contacts are displayed on the map as black dots, however map contacts and TDC is on at easy difficulty levels unlike in RFO. Campaign has more realistic traffic amounts. Dud torpedoes are a real problem for Americans in WW2 pacific theater submarines, it is featured in this mod.
</p>

<p>Manual has great deal of good information for tactics and equipment, well worth the read.
</p>

