<h2>It's History</h2>
<p>My earliest submarine faschination memories from my childhood are when I was reading some WWII magazines in shop (too cheap/poor to purchase them) about submarine attacks on surface ships, I was really interested about ocean underwater world and submarines. It was quite while before I got to play my first submarine simulation.
</p>

<p>Submarine Simulations below appear in order they were released over the years, oldest first.
</p>

<h2>Gato</h2>
<p>1983 heh, the first submarine simulation I remember playing was Gato in my friends fathers computer, it was such an magical feeling to destroy ships with a submarine in a computer game. I bet my friends parents were frustrated with me as I always asked to get to visit them to play Gato without having any political correctness and consideration about visiting, that I cant just park myself to their house to play this game all day, hehe good stuff man.
</p>

<p>Gato was at that time some CGA based graphics or something, very crude stuff.
</p>

<h2>Silent Service Commodore 64</h2>
<p>Loading a C cassette tape into my Commodore 64 setup and typing in LOAD "*",8,1 was another magical milestone in my computer history. Damn it takes me back recalling those long days and nights I spend just playing this game hunting the jap ships.
</p>

<h2>Silent Service Amiga</h2>
<p>Don't remember that much about Silent Service on Amiga 500, but I did play it on it though.
</p>

<h2>The Hunt for Red October</h2>
<p>Don't remember much about this, but I do have some VHS tape video captures still on my HDD where I'm doing stuff in my room and I have this games box/cover next to my Commodore 64 :)
</p>

<h2>Red Storm Rising</h2>
<p>Also very faint memories from this game too, dunno where my mind was at this era as dont remember much. But can you blame me, its been 30 damn years hehe! Also this and hunt for red october both are shrouded in same vague memory, I might even confuse them both. However I played both games on Commodore 64 and had a great fun.
</p>

<h2>Aces of The Deep</h2>
<p>This game was really good after Silent Service as it has more complex campaign, or something like that. Again don't remember much about it, I have some recollection about good looking ocean waves that actually move etc, as Silent Service before that had just static type horizon level water graphic.
</p>

<p><b>Command: Aces of The Deep</b> was re-release of the game, don't remember much but I recall playing both. This re-release was just some texture upgrades and small stuff like that.
</p>

<h2>Silent Hunter (1)</h2>
<p>The first game in the series was made by SSI, it was in the 1990's DOS era, heh the good old times. I played this a lot, also it got some patrol disk releases three total.
</p>

<h2>688(I) Hunter/Killer</h2>
<p>Jane's Combat Simulations (developed by Sonalysts) excellent modern nuclear submarine simulation of course is huge change from the World War II steam junk, it is also close to my heart, in fact I kind of like nuclear sub sims better than the ancient WWII stuff but its just the fact that today (2016) we basically have none since these Janes/Sonalysts simulations have been outdated years ago :(
</p>

<h2>Fleet Command</h2>
<p>Jane's Fleet Command was awesome... but not a submarine simulator, err so why do I mention it then huh? Well it was big part of my gaming history and you could control submarines in very generic way and see them in 3D dive under the ocean surface, so its worth to mention. Also there is Harpoon but its only 2D so that cant be included in submarine simulation page.
</p>

<h2>Silent Hunter 2</h2>
<p>I remember waiting this release for so bad it was unbearable, I was so happy when I finally got the game... but somehow all that expectaction ruined it for me, it was not really a good submarine game for me and I got very little play time.
</p>

<h2>Sub Command</h2>
<p>This was an upgrade for good old 688(I) Hunter/Killer, you get to dive with Sea Wolf class SSN plus with some russian sub (which I had little interest of). I spend lot of time with Sub Command that's for sure.
</p>

<p>It sounds stupid but I really loved surfacing and diving the SSN 688 in this game, heh no idea why. Also all the high tech warfare was so cool, electronic surveillance measures like putting out that floating wire to catch radio messages, etc. Target Motion Analysis (TMA) to make those target plots was fun, always trying to figure out what contact are you tracking in sonar, was really faschinating stuff.
</p>

<h2>Dangerous Waters</h2>
<p>Dangerous Waters also included Oliver Hazard Perry surface ship which was quite nice although I recall the ship being bit boring dunno why.
</p>

<p>I actually had an large naval warfare campaign planned for Dangerous Waters, with mission manuscript and all, but then other games like OFP/ArmA with modding came along and took all my time.
</p>

<h2>Silent Hunter 3</h2>
<p>This was the first real Silent Hunter simulator we know them today (2016). I was waiting for this like crazy and was so happy when the release came and I got my DVD handed to me (it is another crazy story how I got the box, maybe I'll write about it sometime).
</p>

<p>Silent Hunter 3 was and still is awesome U-Boat simulator. I had no complaints when I was playing it, was just great time enjoying the atlantic submarine warfare.
</p>

<h2>Silent Hunter 4</h2>
<p>Again nothing had changed I waited this release like crazy and getting the release to my HDD was beyond madness heh (maybe sometime I'll write about it).
</p>

<p>However when I launched the game the first time, I got surprise as my computer did not manage to run it smoothly... it did run it, but it was such a low performance stutter that you could not enjoy it. I turned all the details to minimum or disabled but still it ran slow, my computer was just simply outdated as it was from 2004 so over three years old. So unfortunately I left SH4 playing pretty much after few days of trying it out.
</p>

<p>On summer of 2017 finally purchased Silent Hunter 4 and played little bit. It does show its age, but its still nice pacific American theme as its been for over a decade just U-Boot jawohl german stuff in atlantic.
</p>

<h2>Silent Hunter 5</h2>
<p>Again I'm sounding like a broken record, I waited this release like crazy, watching all the preview videos, screenshots etc. Then I had little bit of problems getting it (again, maybe one day I write about it), but finally got it on March 3rd, 2010. It was instant hit, SH5 is so great there is no comparison (well literally so heh).
</p>

<p>This was 2010 and it wasnt all smooth sailing, my computer did manage to run the game in decent FPS so I could enjoy it... but my power supply unit (PSU) was underpowered for my graphics card and everytime there was big graphical effects like explosions etc, my computer simply shut down, CLICK.
</p>

<p>Haha that was quite frustrating. Also worth to note that I was really slow to get on board with the computer screen revolution so I was running my computer with 4:3 aspect 19" CRT screen, the horror heh. I recall running 1024x768 desktop resolution at this time.
</p>

<p>On early 2011 I got new PSU and 16:9 aspect 24" 1920x1080 resolution monitor. When I launched SH5 the first time with that screen it felt like a brand new game even though I just played it less than a year ago. No problems with PSU anymore and I could fully enjoy those long patrols hunting british ships in my U-Boot, was excellent.
</p>

<p>On spring of 2015 I upgraded to 32" 4k 3840x2160 resolution monitor and oh my, SH5 looks so good its difficult to put in words! :)
</p>

<p>Since then I completed my first full SH5 campaign using the Lite LC campaign and few other mods. I made a few videos of this to <a href="https://www.youtube.com/user/pmcsince1984">PMC youtube channel</a> so check them out.
</p>

<h2>Cold Waters</h2>
<p>Cold Waters was released 2017. Played it a bit, but had kind of mixed feelings, in overall its OK but still when you get right down to it... Cold Waters is just so arcadey. It was not until the South China Sea upgrade was released when I really got into Cold Waters. In summer of 2018 I played a lot as you can see from <a href="https://www.youtube.com/playlist?list=PLKt0kt4BAefksdYFKYmp4G7xTUK4-wUrO" target="_blank">PMC Cold Waters youtube playlist</a> heh.
</p>

